
public class Hesap {

	String tcno;
	String okulno;
	
	public Hesap(String tcno, String okulno) {
		
		this.tcno = tcno;
		this.okulno = okulno;
	}

	public String getTcno() {
		return tcno;
	}

	public void setTcno(String tcno) {
		this.tcno = tcno;
	}

	public String getOkulno() {
		return okulno;
	}

	public void setOkulno(String okulno) {
		this.okulno = okulno;
	}
	
	
	
	
}
